const LANGUAGE_STORAGE_KEY = "monteboro-language";

function getStoredLanguage() {
  try {
    const storedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return storedLang && translations[storedLang] ? storedLang : "it";
  } catch (error) {
    return "it";
  }
}

function applyLanguage(lang) {
  const normalizedLang = translations[lang] ? lang : "it";
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[normalizedLang] && translations[normalizedLang][key]) {
      element.textContent = translations[normalizedLang][key];
    }
  });
  document.documentElement.lang = normalizedLang;
  setLanguageButtonLabel(normalizedLang);

  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, normalizedLang);
  } catch (error) {
    // Ignore storage errors
  }
}

function setLanguageButtonLabel(lang) {
  const langToggle = document.querySelector(".lang-toggle");
  const labels = { it: "Italiano", en: "English", de: "Deutsch" };
  if (langToggle) {
    langToggle.textContent = labels[lang] || "Lingua";
  }
}

function closeLanguageMenu() {
  const langMenu = document.querySelector(".lang-menu");
  const langToggle = document.querySelector(".lang-toggle");
  if (langMenu) langMenu.classList.remove("open");
  if (langToggle) langToggle.setAttribute("aria-expanded", "false");
}

document.addEventListener("DOMContentLoaded", function () {
  const navToggle = document.querySelector(".nav-toggle");
  const mainNav = document.querySelector(".main-nav");
  const langToggle = document.querySelector(".lang-toggle");
  const langMenu = document.querySelector(".lang-menu");
  const langButtons = document.querySelectorAll(".lang-dropdown button");

  const defaultLang = getStoredLanguage();
  applyLanguage(defaultLang);

  if (navToggle && mainNav) {
    navToggle.addEventListener("click", function () {
      const expanded = this.getAttribute("aria-expanded") === "true";
      this.setAttribute("aria-expanded", String(!expanded));
      mainNav.classList.toggle("open");
    });
  }

  if (langToggle && langMenu) {
    langToggle.addEventListener("click", function (event) {
      event.stopPropagation();
      const expanded = this.getAttribute("aria-expanded") === "true";
      this.setAttribute("aria-expanded", String(!expanded));
      langMenu.classList.toggle("open");
    });
  }

  langButtons.forEach((button) => {
    button.addEventListener("click", function () {
      const lang = this.getAttribute("data-lang");
      if (!lang) return;
      applyLanguage(lang);
      closeLanguageMenu();
    });
  });

  document.addEventListener("click", function (event) {
    if (!langMenu?.contains(event.target)) {
      closeLanguageMenu();
    }
  });
});
